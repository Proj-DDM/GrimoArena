#ifndef TITLESTATE_H
#define TITLESTATE_H

#include "Utility/SceneSupport/IScene.h"

class TitleState : public IScene {
public:
	/*-------------------------------------------------------------------
	|	・コンストラクタ
	-------------------------------------------------------------------*/
	TitleState() = default;

	/*-------------------------------------------------------------------
	|	・デストラクタ
	-------------------------------------------------------------------*/
	~TitleState() {};

	/*-------------------------------------------------------------------
	|	・レイヤーの生成
	-------------------------------------------------------------------*/
	static TitleState* create(cocos2d::Layer* layer);

	/*-------------------------------------------------------------------
	|	・初期化
	--------------------------------------------------------------------*/
	bool init(cocos2d::Layer* layer) override;

	/*--------------------------------------------------------------------
	|	・更新
	|　 float at	１フレームにかかる時間
	--------------------------------------------------------------------*/
	void update(float at) override;

	/*--------------------------------------------------------------------
	|	・シーン始め
	-------------------------------------------------------------------*/
	void fadeIn(float at) override;


	/*------------------------------------------------------------------
	|	・ゲームメイン更新
	-------------------------------------------------------------------*/
	void sceneMain(float at) override;

	/*------------------------------------------------------------------
	|	・シーン終了
	------------------------------------------------------------------*/
	void fadeOut(float at)   override;

	/*----------------------------------------------------------------------
	|	・タッチ始め
	----------------------------------------------------------------------*/
	bool onTouchBegan(cocos2d::Touch* touch, cocos2d::Event* event);

	/*----------------------------------------------------------------------
	|	・タッチ終わり
	----------------------------------------------------------------------*/
	void onTouchEnded(cocos2d::Touch* touch, cocos2d::Event* event);

private:
	/*------------------------------------------------------------------
	|	メインループ始め
	-------------------------------------------------------------------*/
	void mainStart(float at) override;

	/*------------------------------------------------------------------
	|	ゲームメイン更新
	-------------------------------------------------------------------*/
	void mainLoop(float at)  override;

	/*------------------------------------------------------------------
	|	メインループ終了
	------------------------------------------------------------------*/
	void mainEnd(float at)   override;

private:
	int mCount;
};
#endif